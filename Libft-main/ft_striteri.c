void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	unsigned int	x;

	x = 0;
	if (s == 0 || f == 0)
		return ;
	while (s[x])
	{
		(*f)(x, &s[x]);
		x++;
	}
}
